// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
  var result = b;
  if (a < b)
    result = a;
  return result;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
if (num % 2 === 0) {
  return true;
} else {
  return false;
} 
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
  var count = 0;
  for (var i = 0; i < str.length; i++){
    if (str.charAt(i) === char){
      count++;
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  var count = 0;
  for (var i = 0; i < str.length; i++){
    if (str.charAt(i) === "B"){
      count++;
    }
  }
  return count;
}

